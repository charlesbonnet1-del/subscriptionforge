import type { CourseContent, Lesson } from "@/lib/content-types";

// Helper pour générer un cas au format canonique sans répéter la structure.
function caseLesson(
  index: number,
  slug: string,
  title: string,
  subtitle: string,
  context: string,
  decision: string,
  results: { value: string; label: string }[],
  takeaways: string[],
  decisionTitle = "La décision",
): Lesson {
  return {
    slug,
    index,
    ref: `Cas ${index}`,
    title,
    subtitle,
    minutes: 6,
    blocks: [
      { type: "lead", text: context },
      { type: "callout", tone: "fire", title: decisionTitle, text: decision },
      { type: "keynumbers", items: results },
    ],
    takeaways,
  };
}

